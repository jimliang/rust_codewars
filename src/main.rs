fn my_first_interpreter(code: &str) -> String {
    let mut result = "".to_string();
    let mut tmp: u8 = 0;
    for c in code.chars() {
        match c {
            '+' => tmp = tmp.wrapping_add(1),
            '.' => result.push(tmp as char),
            _ => ()
        }
    }
    result
}

